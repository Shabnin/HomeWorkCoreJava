package hw1JavaBasic;

/*
 *Project names generally starts with UpperCase --> HomeWorkCoreJava
Package names generally starts with lowerCase --> hw1JavaBasic
Class names starts with UpperCase.  --> MySelf
Class names are usually nouns or noun phrases
Project, package, Class follow Camel case or Snake case pattern. 
 
Camel Case Feature------ 
It starts from second word. There is No space between words, second word start with upper case, and same for third, fourth, fifth
Camel case example for a Project --> HomeWorkCoreJava
Camel case example for a package --> hw1JavaBasic
Camel case example for a Class --> MySelf

Snake Case Feature-------
Here, words are separated by under score and from second word always lower case.
Snake case feature considered from second word, (not from the first word)
Snake case example for a Project --> Home_work_core_java
Snake case example for a package --> hw1_java_basic
Snake case example for a Class --> My_self
 */
